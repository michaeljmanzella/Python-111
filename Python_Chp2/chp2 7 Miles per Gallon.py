# 7. Miles-per-Gallon

# A car's miles-per-gallon (MPG) can be calculated with the following formula:
# MPG = Miles driven / Gallons of gas used
# Write a program that asks the user for the number of miles driven and the
# gallons of gas used. It should calculate the car's MPG and display the result.

milesDriven = int(input('How many miles: '))
gallonsUsed = int(input('How many gallons used: '))
mpg = milesDriven / gallonsUsed
print('The mpg is',mpg)

# Output
# How many miles: 60
# How many gallons used: 2
# The mpg is 30.0


