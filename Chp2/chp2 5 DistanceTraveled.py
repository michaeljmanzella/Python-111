# 5. Distance Traveled
# Assuming there are no accidents or delays, the distance that a
# car travels down the interstate can be calculated with the following formula:
# Distance = Speed x Time

# A car is traveling at 70 miles per hour. Write a program that displays the following:
# The distance the car will travel in 6 hours
# The distance the car will travel in 10 hours
# The distance the car will travel in 15 hours

distance = 70 * 6
print('The distance the car will travel in 6 hours is', distance , 'miles' )
distance = 70 * 10
print('The distance the car will travel in 10 hours is', distance , 'miles'  )
distance = 70 * 15
print('The distance the car will travel in 15 hours is', distance , 'miles'  )

# Output
# The distance the car will travel in 6 hours is 420 miles
# The distance the car will travel in 10 hours is 700 miles
# The distance the car will travel in 15 hours is 1050 miles
